				<p>
				<h2>Alimentação saudável: dicas</h2>
				Durante nosso dia temos diversas responsabilidades, e muitas vezes não sobra tempo para nós mesmos nem mesmo para adquirirmos uma alimentação saudável. Mas é preciso ter atenção, pois é muito importante cuidarmos da nossa saúde,
				e não precisamos radicalizar para isso. Continue a leitura e confira 7 dicas para você manter uma alimentação saudável!</p><br>
				<p>
				<h2>1. Alimente-se devagar</h2>
				Alimentar-se rápido demais nos deixa a sensação de peso, de sono, e faz com que comamos mais do que o necessário também. Para evitar isso, desligue aparelhos durante as refeições,
				pois estes podem disparar a ansiedade e fazer você comer depressa.
				E sempre que possível, almoce na companhia de alguém, para que possam conversar e saborear a comida, fazendo sua refeição mais agradável.</p><br>
				<p>
				<h2>2. Beba, no mínimo, 2 litros de água por dia</h2>
				Precisamos de água pois nosso sangue é composto por 90% dela, e nosso corpo pode variar entre 50% e 75%, então cada vez que perdemos líquido (suor, etc), é preciso repor.
				Além do mais, a água faz uma limpeza em todo nosso organismo, hidrata nossa pele, e é por ela que vitaminas e minerais chegam nas células de todo nosso corpo.</p><br>
				<p>
				<h2>3. Reduza os níveis de açúcar</h2>
				O açúcar refinado é um composto que vem da cana-de-açúcar, porém, toda a parte boa dele foi retirada, sobrando apenas as calorias. Reduzir significa deixar de ingerir uma quantidade de calorias desnecessárias para nosso organismo.
				Procure também substituir a versão refinada pelo mascavo, por exemplo, que não passou pelo processo de refinamento, e ainda mantém os valores nutricionais.</p><br>

				<p>
				<h2>4. Inclua alimentos orgânicos em sua alimentação</h2>
				Estes são preferidos pois são produzidos sem utilização de agrotóxicos e conservantes, que são substâncias maléficas para o nosso organismo. Caso não seja possível encontrar nestas versões, sempre ao escolher frutas, verduras e legumes,
				pense em como você as encontraria se estivesse pegando direto do pé. Se oferecerem a você um tomate muito vermelho, muito redondo, de casca lisa e brilhante, fique esperto!</p><br>
				<p>
				<h2>5. Coma a salada primeiro</h2>
				No almoço e no jantar sempre comece suas refeições com a salada, especialmente as saladas frias de folhas verdes, pois são ricas em nutrientes e vitaminas, e são excelentes fontes de fibras que conferirão saciedade,
				assim quando chegar no prato principal, evitará que você coma além da conta. E quando for escolher a salada, quanto mais colorida ela for, melhor!</p><br>
				<p>
				<h2>6 Consuma frutas todos os dias</h2>
				As frutas são excelentes alimentos, pois são ricas em fibras, vitaminas, minerais, e atuam sempre de forma benéfica em nosso organismo. Consuma, no mínimo, 3 porções por dia, e sempre que possível variando as frutas.
				Assim você poderá desfrutar de todos os benefícios que elas apresentam.</p><br>
				<p>
				<h2>7. Faça de 4 a 6 refeições por dia</h2>
				Assim, você evita ter picos de fome durante o dia, o que te faria ter vontade de comer mais do que o necessário na próxima refeição. Fazendo desta forma,
				seu organismo estará sempre bem nutrido, e você comerá somente o necessário. Lembre-se que, quanto mais refeições você fizer ao dia, menor será cada porção.</p><br>






